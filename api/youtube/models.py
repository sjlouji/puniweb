    invoice_id =  models.CharField(max_length=200,  null=True)
    international = models.BooleanField(default=False)
    method = models.CharField(max_length=200,  null=True)
    captured =  models.BooleanField(default=False)
    description = models.TextField(null=True)
    card_id  =  models.CharField(max_length=200,  null=True)
    bank = models.CharField(max_length=200,  null=True)
    wallet = models.CharField(max_length=200,  null=True)
    vpa = models.CharField(max_length=200,  null=True)
    email  = models.EmailField(null=True)
    contact = models.CharField(max_length=200,  null=True)
    fee = models.CharField(max_length=200,  null=True)
    tax = models.CharField(max_length=200,  null=True)
    error_code = models.CharField(max_length=200,  null=True)
    error_description = models.CharField(max_length=200,  null=True)
    error_source = models.CharField(max_length=200,  null=True)
