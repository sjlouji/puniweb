from django.db import models

# Create your models here.

class Youtube(models.Model):
    youtube_id = models.CharField(max_length=200)
    youtube_etag = models.CharField(max_length=200, unique=True)
    kind = models.CharField(max_length=50)
    publishedAt = models.TextField()
    channelId = models.CharField(max_length=200)
    title = models.TextField()
    description = models.TextField()
    thumbnailsDefault = models.URLField(max_length=200)
    thumbnailsHight = models.URLField(max_length=200)
    channelTitle  = models.TextField()
    playlistId = models.CharField(max_length=200)
    position = models.IntegerField()
    videoId = models.CharField(max_length=100)
    stats_id = models.CharField(max_length=200, unique=True,default="null")
    stats_etag = models.CharField(max_length=200, unique=True, default="null")    
    viewCount = models.IntegerField(default=0)
    likeCount = models.IntegerField(default=0)
    dislikeCount = models.IntegerField(default=0)
    favoriteCount = models.IntegerField(default=0)
    commentCount = models.IntegerField(default=0)
    transcript  =  models.TextField(default="")



